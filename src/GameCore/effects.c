uint16_t CalcDamage(EntityId creatureID, uint16_t abilityPower)
{
    uint8_t level = g_run.creatures.level[creatureID].value;
    uint16_t ratio = 100 / level;
    uint16_t base = (g_run.creatures.stats[creatureID].attack / ratio);
    uint16_t skill = (abilityPower / ratio);
    uint16_t mod = GetNibble(g_run.creatures.stat.strength, creatureID);

    uint16_t damage = base + skill + mod;
    if (damage == 0) damage = 1;
    return damage;
}

/**********************************************************************************************************************/
/*
**********************************************************************************************************************/
uint16_t CalcHeal(EntityId creatureID, uint16_t abilityPower)
{
    uint16_t damage = g_run.creatures.stats[creatureID].magic + abilityPower;
    if (damage == 0) damage = 1;
    return damage;
}


/********************************************************************************************************************************************************************************************************************************************/
/*
/********************************************************************************************************************************************************************************************************************************************/
void DoDamage(EntityId creatureID, uint16_t damage)
{
    uint16_t hp = Int999GetCurrent(&g_run.creatures.hp[creatureID]);
    hp = (hp > damage) ? hp - damage : 0;
    PrintCombatLog(creatureID, damage);
    Int999SetCurrent(&g_run.creatures.hp[creatureID], hp);
}

/**********************************************************************************************************************/
/*
**********************************************************************************************************************/
bool HealTarget(EntityId e_id, uint16_t value)
{
    uint16_t heal = CalcHeal(e_id, value);

    if (e_id == NO_CREATURE) return false;
    IntMax999 hp = g_run.creatures.hp[e_id];
    uint16_t cur = Int999GetCurrent(&hp);
    uint16_t max = Int999GetMax(&hp);
    if (cur == max) return false;
    cur = (cur + heal > max) ? max : cur + heal;
    Int999SetCurrent(&hp, cur);
    g_run.creatures.hp[e_id] = hp;

    return true;
}

/**********************************************************************************************************************/
/*  Apply the bonus damage of same type attack
/*  Apply the bonus/reduction of attack vs creature types
 *  Apply the damage reduction from defence
**********************************************************************************************************************/
uint16_t CalcModifier(EntityId attackerID, EntityId defenderID, Type attackType, uint16_t damage)
{
    Creature creature_type1 = GetCreatureType(attackerID);
    Type typeA = g_gameFlash.gameData.creatureTypes[creature_type1].typeA;
    Type typeB = g_gameFlash.gameData.creatureTypes[creature_type1].typeB;

    if (typeA == attackType || typeB == attackType)
        damage = (float)damage * 1.25f;

    Creature creature_type2 = GetCreatureType(defenderID);
    uint16_t base = (g_run.creatures.stats[defenderID].defence / 15);
    uint16_t mod = GetNibble(g_run.creatures.stat.defence, defenderID);

    Type typeC = g_gameFlash.gameData.creatureTypes[creature_type2].typeA;
    Type typeD = g_gameFlash.gameData.creatureTypes[creature_type2].typeB;

    int8_t mult_a = g_gameFlash.gameData.typeEffects[(attackType * TYPE_COUNT) + typeC];
    int8_t mult_b = g_gameFlash.gameData.typeEffects[(attackType * TYPE_COUNT) + typeD];

    float mult1 = ((float)mult_a + (float)mult_b) / 200.0f;
    if (mult1 != 0)
    {
        float f_damage = (float)damage + ((float)damage * mult1) - (float)base - (float)mod;
        damage = (uint16_t)f_damage;
    }

    if (damage == 0) damage = 1;
    return damage;
}

